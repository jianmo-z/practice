//leetcode submit region begin(Prohibit modification and deletion)
func canJump(nums []int) bool {
	if len(nums) == 0 {
		return true
	}
	size := len(nums)
	maxRange := 0
	for i := range nums {
		if i <= maxRange {
			if maxRange < i+nums[i] {
				maxRange = i + nums[i]
			}
			if maxRange >= size-1 {
				return true
			}
		} else {
			return false
		}
	}

	return false
}

